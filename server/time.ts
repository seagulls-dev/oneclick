export const oneSecond  = 1000;
export const oneMinute = 1000*60
export const oneHour = 1000*60*60;
export const oneDay = 1000*60*60*24;

export const oneWeek = oneDay * 7;
export const oneMonth = oneWeek * 4;
export const oneQuarter = oneMonth * 4;
export const oneYear = oneMonth * 12;
export const oneDecade = oneYear * 10;